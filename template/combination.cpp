const ll INF = 1LL << 60;
const int MAX = 510000;
const int MOD = 1000000007; //問題によって変える

ll fac[MAX], finv[MAX], inv[MAX];

// 前処理
// 計算量O(n)
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;

    for(int i = 2; i < MAX; i++){
        fac[i] = fac[i-1] * i % MOD;
        inv[i] = MOD - inv[MOD % i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
// 計算量O(1)
ll COM(int n, int k){
    if(n < k)return 0;
    if(n < 0 || k < 0)return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}